x=1 #atamayı sağdan sola yapar
# += kendi değerini sağdaki değer ile toplar tekrar yazar
puts x +=2

# -= kendi değerini sağdaki değer ile çıkarır tekrar yazar
puts x-=2

# *= kendi değerini sağdaki değer ile çarpar tekrar yazar
puts x*=4

# /= kendi değerini sağdaki değer ile böler tekrar yazar
puts x/3.to_f

x=1
puts x # x'in değeri tekrar güncellenebilir
